package day03_operator;

public class Ex04_Op {

	/*
	 # 관계 연산자
	  - 결과값 : true(참) or false(거짓)
	  - example
	     a=10,b=20;
	     a>b  : false
	     a<=b : true
	     a==b : false
	     a!=b : true
	 */
	
	public static void main(String[] args) {
		
		double do1=3.0, do2=3.1;
		System.out.println(do1>do2);
		System.out.println(do1<=do2);
		System.out.println(do1==do2);
		System.out.println(do1!=do2);
		
	}
}
